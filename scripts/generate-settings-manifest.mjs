#!/usr/bin/env node

/**
 * Scans the setting directory under src/app/(secure)/system/setting/
 * and writes an updated manifest to src/lib/settings-manifest.ts.
 *
 * Usage:
 *   node scripts/generate-settings-manifest.mjs
 *
 * Add to package.json scripts for convenience:
 *   "gen:settings": "node scripts/generate-settings-manifest.mjs"
 */

import { readdirSync, writeFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const settingDir = join(root, "src", "app", "(secure)", "system", "setting");
const outputFile = join(root, "src", "lib", "settings-manifest.ts");

function slugToLabel(slug) {
  return slug.replace(/[-_]/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
}

if (!existsSync(settingDir)) {
  console.error(`Setting directory not found: ${settingDir}`);
  process.exit(1);
}

const entries = readdirSync(settingDir, { withFileTypes: true }).filter((d) => d.isDirectory()).map((d) => d.name).sort();
const items = entries.map((slug) => ({ slug, label: slugToLabel(slug), href: `/system/setting/${slug}` }));

const content = `export interface SettingSubItem {
  /** Directory/folder name used as the URL segment */
  slug: string;
  /** Human-readable display label */
  label: string;
  /** Full route path */
  href: string;
}

/**
 * Build-time manifest of setting subdirectories.
 *
 * Auto-generated by scripts/generate-settings-manifest.mjs
 * on ${new Date().toISOString().slice(0, 10)}.
 *
 * To regenerate, run:  node scripts/generate-settings-manifest.mjs
 */
export const settingsManifest: SettingSubItem[] = ${JSON.stringify(items, null, 2)};

/** Convert a slug like "my-page" into a title like "My Page" */
export function slugToLabel(slug: string): string {
  return slug
    .replace(/[-_]/g, " ")
    .replace(/\\b\\w/g, (c) => c.toUpperCase());
}
`;

writeFileSync(outputFile, content, "utf-8");
console.log(`Wrote ${items.length} setting items to ${outputFile}`);
